text = input('Введіть довільний рядок: ')

print(f'Третій символ рядка "{text}": ', text[2])
print(f'Передостанній символ рядка "{text}":', text[-2])
print(f'Перші п\'ять символів рядка "{text}":', text[:5])
print(f'Рядок "{text}" без двох останніх символів:', text[:-2])
print(f'Усі символи рядка"{text}" з парними індексами:', text[::2])
print(f'Усі символи рядка"{text}" з непарними індексами:', text[1::2])
print(f'Усі символи рядка"{text}" у зворотньому порядку:', text[::-1])
print(f'Усі символи рядка"{text}" у зворотньому порядку через один:', text[::-2])
print(f'Рядок "{text}" має довжину {len(text)} символів')
